import lk.game.cocktails.R

class ModeFragment : Fragment() {

    private lateinit var viewModel: ModeViewModel

    override fun onCreateView(
        inflater: LayoutInflater,
        container: ViewGroup?,
        savedInstanceState: Bundle?
